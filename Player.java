package encaraMessi;

public class Player {
	
	
	private int hand = 0;
	private int handScore = 0;
	private int place = 0;
	boolean isAI = false;
	boolean isReady = false;
	
	public Player() {
	
		
		
	}



	public int getHand() {
		return hand;
	}



	public void setHand(int hand) {
		this.hand = hand;
	}



	public int getPlace() {
		return place;
	}



	public void setPlace(int place) {
		this.place = place;
	}



	public int getHandScore() {
		return handScore;
	}



	public void setHandScore(int handScore) {
		this.handScore = handScore;
	}
	
	
}
